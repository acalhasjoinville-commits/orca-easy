function useSAMutations() {
  const qc = useQueryClient();
  const invalidateAll = () => {
    qc.invalidateQueries({ queryKey: ["sa-dashboard"] });
    qc.invalidateQueries({ queryKey: ["sa-empresas"] });
    qc.invalidateQueries({ queryKey: ["sa-users"] });
    qc.invalidateQueries({ queryKey: ["sa-invites"] });
    qc.invalidateQueries({ queryKey: ["sa-audit"] });
    qc.invalidateQueries({ queryKey: ["sa-empresa-detail"] });
  };
  const updateEmpresaStatus = useMutation({
    mutationFn: (args) => rpc("sa_update_empresa_status", { _empresa_id: args.empresaId, _new_status: args.newStatus }),
    onSuccess: () => {
      ue.success("Status da empresa atualizado");
      invalidateAll();
    },
    onError: (error) => ue.error(getErrorMessage(error, "Erro ao atualizar status"))
  });
  const createEmpresa = useMutation({
    mutationFn: (args) => rpc("sa_create_empresa", {
      _nome_fantasia: args.nome_fantasia,
      _razao_social: args.razao_social || "",
      _cnpj_cpf: args.cnpj_cpf || "",
      _email_contato: args.email_contato || "",
      _telefone: args.telefone || "",
      _invite_email: args.invite_email || null,
      _invite_role: args.invite_role || "admin"
    }),
    onSuccess: () => {
      ue.success("Empresa criada com sucesso");
      invalidateAll();
    },
    onError: (error) => ue.error(getErrorMessage(error, "Erro ao criar empresa"))
  });
  const upsertUserRole = useMutation({
    mutationFn: (args) => rpc("sa_upsert_user_role", { _user_id: args.userId, _empresa_id: args.empresaId, _role: args.role }),
    onSuccess: () => {
      ue.success("Papel atualizado");
      invalidateAll();
    },
    onError: (error) => ue.error(getErrorMessage(error, "Erro ao atualizar papel"))
  });
  const deleteUserRole = useMutation({
    mutationFn: (args) => rpc("sa_delete_user_role", { _user_id: args.userId, _empresa_id: args.empresaId }),
    onSuccess: () => {
      ue.success("Papel removido");
      invalidateAll();
    },
    onError: (error) => ue.error(getErrorMessage(error, "Erro ao remover papel"))
  });
  const createInvite = useMutation({
    mutationFn: (args) => rpc("sa_create_invite", { _empresa_id: args.empresaId, _email: args.email, _role: args.role }),
    onSuccess: () => {
      ue.success("Convite criado");
      invalidateAll();
    },
    onError: (error) => ue.error(getErrorMessage(error, "Erro ao criar convite"))
  });
  const revokeInvite = useMutation({
    mutationFn: (inviteId) => rpc("sa_revoke_invite", { _invite_id: inviteId }),
    onSuccess: () => {
      ue.success("Convite revogado");
      invalidateAll();
    },
    onError: (error) => ue.error(getErrorMessage(error, "Erro ao revogar convite"))
  });
  const approveUser = useMutation({
    mutationFn: (args) => rpc("sa_approve_user", {
      _user_id: args.userId,
      _empresa_id: args.empresaId,
      _role: args.role || "vendedor"
    }),
    onSuccess: () => {
      ue.success("Usuario aprovado");
      invalidateAll();
    },
    onError: (error) => ue.error(getErrorMessage(error, "Erro ao aprovar usuario"))
  });
  return {
    updateEmpresaStatus,
    createEmpresa,
    upsertUserRole,
    deleteUserRole,
    createInvite,
    revokeInvite,
    approveUser
  };
}
